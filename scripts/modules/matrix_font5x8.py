GLYPH_WIDTH = 5
GLYPH_HEIGHT = 8
CHAR_STEP = 6  # 5 столбцов + 1 пробел, как в GyverGFX


def char_index(ch: str) -> int:
    """Индекс глифа в CHAR_MAP (UTF-8 / Unicode, как GyverGFX font5x8)."""
    if not ch:
        return 0
    code = ord(ch)
    if 0x20 <= code <= 0x7E:
        return code - 0x20
    if 0x0410 <= code <= 0x042F:
        return 95 + code - 0x0410
    if code == 0x0401:
        return 95 + ord("Е") - 0x0410
    if 0x0430 <= code <= 0x044F:
        return 127 + code - 0x0430
    if code == 0x0451:
        return 159
    if code in (0x2013, 0x2014):
        return ord("-") - 0x20
    return 0


def message_width(message: str) -> int:
    return len(message) * CHAR_STEP


def draw_char(pixels, x: int, ch: str) -> None:
    """Рисует символ в PIL Image mode '1' (putpixel)."""
    cols = CHAR_MAP[char_index(ch)]
    for col, byte_val in enumerate(cols):
        for row in range(GLYPH_HEIGHT):
            if byte_val & (1 << row):
                pixels.putpixel((x + col, row), 255)


def draw_message(pixels, x: int, message: str) -> None:
    for ch in message:
        draw_char(pixels, x, ch)
        x += CHAR_STEP
